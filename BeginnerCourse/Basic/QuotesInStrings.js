// Using single quotes or double quotes
var myStr = 'I am a "double quoted" string inside "double quotes"'
console.log(myStr)

// Using \"
myStr = "I am a \"double quoted\" string inside \"double quotes\""
console.log(myStr)

// Using ` `
myStr = `'I am a "double quoted" string inside "double quotes"'`;
console.log(myStr)